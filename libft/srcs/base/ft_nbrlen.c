#include "libft.h"

int	ft_nbrlen(int n)
{
	if (n < 100000)
	{
		if (n < 100)
			return (n < 10 ? 1 : 2);
		else
		{
			if (n < 1000)
				return (3);
			else
				return (n < 10000 ? 4 : 5);
		}
	}
	else
	{
		if (n < 10000000)
			return (n < 1000000 ? 6 : 7);
		else
		{
			if (n < 100000000)
				return (8);
			else
				return (n < 1000000000 ? 9 : 10);
		}
	}
	return (-1);
}
